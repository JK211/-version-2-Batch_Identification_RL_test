class Multi(object):
    def __init__(self, d):
        self.vt = 0
        self.m = 0
        self.count = 0
        self.fixed_d = d
        self.new_Nt = []

    def Multi_Round_Identification(self, Nt, d, last_group_size):
        """ Using the idea of ​​multi-grouping, it is divided into d groups each time and stops until all illegal
        signatures are found. d is the current number of illegal signatures.
        """
        # print("Each recursive input of MRI is:", Nt)
        # if self.count == self.fixed_d:
        if last_group_size == 1:
            # print("The MRI algorithm is over and all illegal signatures have been found!")
            return 0

        elif math.ceil(len(Nt)/d) == last_group_size:
            self.m = self.m + 1
            for i in range(len(Nt)):
                self.count = self.count + 1
                self.vt = self.vt + 1
            group_nums = len(Nt)
            group_size = 1
            self.Multi_Round_Identification(Nt, group_nums, group_size)

        else:
            group_nums = d
            Split_Group = np.array_split(Nt, group_nums)
            group_size = math.ceil(len(Nt)/group_nums)
            self.m = self.m + 1

            temp_Nt = []
            for i in range(len(Split_Group)):
                self.vt = self.vt + 1
                temp = Split_Group[i]
                if np.sum(Split_Group[i])!=0 and len(Split_Group[i])>1:
                    temp_Nt = np.append(temp_Nt, Split_Group[i])
                    # self.new_Nt = np.append(self.new_Nt, Split_Group[i])
                elif len(Split_Group[i]) == 1:
                    self.count = self.count + 1
                    group_nums = group_nums - 1
            self.new_Nt = temp_Nt
            self.Multi_Round_Identification(self.new_Nt, group_nums, group_size)


