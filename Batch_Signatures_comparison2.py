class BatchEnvironment(gym.Env):
    def __init__(self, argv):
        """
        Initialization
        :param argv: ***
        """
        self.argv = argv
        self.batchSig_num = argv.batchSig_num
        self.invalid_Sig_num = argv.invalid_Sig_num

        self.Pt = 0
        self.Nt = []

        self.observation_space = Dict(
            {
                "Invalid_Percent_Pt": Box(low=0, high=100, shape=(1,), dtype=np.float32),  # Percentage of illegal signatures in the current round
                "Sigs_2be_Veri_Size": Discrete(self.batchSig_num),  # Size of the set of signatures to be verified
            }
        )
        # Action space 0 1 2 3, where 0 stands for II, 1 for BSI, 2 for MRI, and 3 for 2-CFF(25,125)
        self.action_space = Discrete(4)


    def step(self, action=None, is_reset=True):
        """
        execute an action
        :param action: Action to be performed
        :return: Returns the current state, a reward, and a boolean value,
                 indicating whether the current episode is over.
        """
        ######################################################################
        #                                                                    #
        #                      init the batch environment                    #
        #                                                                    #
        ######################################################################
        Ct = []  # Set of illegal requests identified in this round
        Mt = []  # Set of requests that passed the identification in this round
        new_Nt = []
        vt = 0  # Number of identification tests required for this round of testing
        # uav_reward = 0
        # uav_observation =[]
        done = False

        ######################################################################
        #                                                                    #
        #            execute action and make a state transition              #
        #                                                                    #
        ######################################################################
        if action == 0:
            # II
            for i in range(len(self.Nt)):
                vt = vt + 1
                if self.Nt[i] == 1:
                    Ct.append(i)
                else:
                    Mt.append(i)
            new_Nt = []
            self.lt = self.lt + vt
            uav_reward = self.get_reward(Mt, Ct, vt, new_Nt)
            uav_observation = self.get_observation(new_Nt, vt)

        elif action == 1:
            # BSI
            low = 0
            high = len(self.Nt)
            mid = int((low + high) / 2)
            Left_Nt = self.Nt[low:mid]
            Rigt_Nt = self.Nt[mid:high]
            if np.sum(Left_Nt) == 0:
                for i in range(len(Left_Nt)):
                    Mt.append(i)
            else:
                new_Nt = np.append(new_Nt, Left_Nt)

            if np.sum(Rigt_Nt) == 0:
                for i in range(len(Rigt_Nt)):
                    Mt.append(mid + i)
            else:
                new_Nt = np.append(new_Nt, Rigt_Nt)
            vt = vt + 2
            self.lt = self.lt + vt
            uav_reward = self.get_reward(Mt, Ct, vt, new_Nt)
            uav_observation = self.get_observation(new_Nt, vt)

        elif action == 2:
            # MRI
            # Step 1: How many illegal signatures are there, split Nt into groups
            # Step 2: Batch authentication each group in turn and record the location of legitimate signatures
            group_nums = int(len(self.Nt) * self.Pt)
            Split_Group = np.array_split(self.Nt, group_nums)
            for i in range(len(Split_Group)):
                vt = vt + 1
                if np.sum(Split_Group[i]) == 0:
                    for j in range(len(Split_Group[i])):
                        Mt.append(i * group_nums + j)
                else:
                    new_Nt = np.append(new_Nt, Split_Group[i])
            self.lt = self.lt + vt
            uav_reward = self.get_reward(Mt, Ct, vt, new_Nt)
            uav_observation = self.get_observation(new_Nt, vt)

        elif action == 3:
            # 2-CFF(25, 125)
            Split_Group = []
            n = 125
            m = 25
            d = 2
            #  Nt is first grouped with a group size of 125
            group_size = n - 1
            Nt = self.Nt
            group_nums = len(Nt) / n
            ceil_group_nums = math.ceil(group_nums)
            for i in range(ceil_group_nums):
                if i < int(group_nums):
                    temp = Nt[(group_size * i + i): (group_size * i + i) + group_size + 1]
                    Split_Group.append(temp)
                else:
                    temp = Nt[(group_size * i + i): (len(Nt) - 1) + 1]
                    Split_Group.append(temp)
            if len(Split_Group[ceil_group_nums - 1]) > n / 2:
                temp = [0] * (n - len(Split_Group[ceil_group_nums - 1]))
                Split_Group[ceil_group_nums - 1] = np.append(Split_Group[ceil_group_nums - 1], temp)
            M = np.load("./Cover free families demos/2-cff25-125.npy")

            all_dist_keys = []
            for item in Split_Group:
                if len(item) == n:
                    valid_set = []
                    invalid_set = []
                    indist_set = []
                    count = 0
                    cff_batch = []
                    new_Nt_keys = []
                    vt = vt + m
                    for i in range(len(M)):
                        # Here they are grouped by 25, with 125 in each group, and numbered for ease of subsequent processing
                        temp = {}
                        for j in range(len(M[i])):
                            if M[i][j] == 1:
                                temp[j] = item[j]
                        cff_batch.append(temp)

                    all_keys = [x for x in range(0, len(M[0]), 1)]
                    count = np.sum(item)
                    for subset in cff_batch:
                        subset_keys = list(subset.keys())
                        subset_values = list(subset.values())

                        if np.sum(subset_values) == 0:
                            valid_set = list(set(valid_set).union(subset_keys))
                    Mt = np.append(Mt, valid_set)

                    if count <= d:
                        invalid_set = list(set(all_keys).difference(valid_set))
                        Ct = np.append(Ct, invalid_set)
                    else:
                        indist_set = list(set(all_keys).difference(valid_set))
                        all_dist_keys = np.append(all_dist_keys, indist_set)
                        new_Nt_keys = np.append(new_Nt_keys, indist_set)
                        indist_set_values = []
                        for x in indist_set:
                            indist_set_values.append(item[x])
                        new_Nt = np.append(new_Nt, indist_set_values)

            if len(Split_Group[len(Split_Group) - 1]) < n:
                new_Nt = np.append(new_Nt, Split_Group[len(Split_Group) - 1])
            self.lt = self.lt + vt
            uav_reward = self.get_reward(Mt, Ct, vt, new_Nt)
            uav_observation = self.get_observation(new_Nt, vt)

        # print("待累加的vt值为：", vt)

        if np.sum(self.Nt) == 0:
            done = True

        return uav_observation, uav_reward, done

    def get_observation(self, new_N_t, v_t):
        """
        Updating the observation space, P_t. P_t is the e% in the paper.
        :param N_t:
        :param e_t:
        :return: Current poisoning percentage P_t,Set of signatures to be verified N_t,Number of detections in current round v_t
        """
        all_state = []
        observation = []
        if len(new_N_t) > 0:
            self.Pt = np.sum(new_N_t) / len(new_N_t)
        else:
            self.Pt = 0
        self.Nt = new_N_t
        self.vt = v_t

        obs = np.append(self.Pt, len(self.Nt))

        return obs

    def get_reward(self, M_t, C_t, v_t, new_Nt):
        reward = len(M_t) * 1 + len(C_t) * 1 - v_t * 1
        # reward = len(C_t) * 1 - v_t * 1
        # reward = 0 - v_t * 5

        if np.sum(new_Nt) == 0:
            reward = reward + (1 / self.lt) * 1000 * 10

        return reward

